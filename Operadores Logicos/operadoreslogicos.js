//Operadores Logicos
//&& (And) - Duas informaçoes verdadeiras

let idade = 20;
let codigoPromocional = true;

//Verificação dos valores
let verficação = idade >= 18 && codigoPromocional;
console.log(verficação);

//|| (or) - só uma das informações tem que ser verdadeira

let verficação2 = idade >= 21 || codigoPromocional;
console.log(verficação2);

//! (not) - ele verfica os valores booleanos e os inverte
