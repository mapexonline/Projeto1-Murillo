//Operaçoes de comparação

//Igualdade
console.log(5 == "5"); //Não Indicado
console.log(5 === "5"); //Indicado

//Estrita igualdade
console.log(5 != "5"); //Não indicado
console.log(5 !== "5"); //Indicado

//Maior que, Menor que, Maior ou igual, Menor ou igual
console.log(5 > "5");
console.log(5 < "5");

//Igual que

console.log(5 >= "5");
console.log(5 <= "5");
